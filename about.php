				<p style="font-size: 18px;">What is this?</p>
				<p>The <em>Arabic/Persian to IJMES Transliterator</em> (or APIT) takes Arabic or Persian text and converts it to transliterated text, as specified by <a href="http://web.gc.cuny.edu/ijmes/docs/TransChart.pdf" target="_blank">the system</a> developed by the International Journal of Middle East Studies (IJMES).</p>

				<p style="font-size: 18px;">Why would anyone need to use this?<p>
				<p>Transliteration is a huge pain, but is necessary for rendering Arabic/Persian text for non-Arabic speakers and for publication in academic journals.</p>

				<p style="font-size: 18px;">How do I use it?</p>
				<p>Enter your Arabic/Persian text into the first box. Select the language you want. Click convert. Currently, it will return the transliterated text without short vowels unless you explicitly write them into the text (e.g. بِسْمِ rather than بسم). You have to enter in the short vowels yourself. Another annoyance right now is that you have to choose whether you want to use the consonant or long vowel version of و and ي. The long vowel will be in parentheses.</p>

				<p style="font-size: 18px;">This doesn't render my Arabic/Persian correctly! What gives?</p>
				<p>Yes, I know. I'm working on it. Check out <a href="http://badhessian.org/2013/06/challenges-of-automated-arabicpersian-transliteration/" target="_blank">this blog post</a> for more details about the challenges to doing this.</p> 

